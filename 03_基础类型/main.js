(function () {
    //布尔类型  ---->boolean
    var flag = true;
    console.log(flag);
    //数字型类型   ---->number
    var a1 = 10; //十进制
    var a2 = 10; //二进制
    var a3 = 10; //八进制
    var a4 = 0xa; //十六进制
    console.log(a1);
    console.log(a2);
    console.log(a3);
    console.log(a4);
    //字符串类型  ---->string
    var str1 = '床前明月光';
    var str2 = '疑是地上霜';
    console.log(str1 + "," + str2);
    //最负川和数字之间能否拼接
    var str = '我的年龄：';
    var age = 20;
    console.log(str + age);
})();
